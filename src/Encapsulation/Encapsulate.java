package Encapsulation;

public class Encapsulate {

	 private String Name; 
	    private int Roll; 
	    private int Age;

	public String getName() {
			return Name;
		}

		public void setName(String name) {
			Name = name;
		}

		public int getRoll() {
			return Roll;
		}

		public void setRoll(int roll) {
			Roll = roll;
		}

		public int getAge() {
			return Age;
		}

		public void setAge(int age) {
			Age = age;
		}

	

}
